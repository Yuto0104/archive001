#ifndef _BULLET2D_H_		// このマクロ定義がされてなかったら
#define _BULLET2D_H_		// 二重インクルード防止のマクロ定義

//*****************************************************************************
// インクルード
//*****************************************************************************
#include "object2D.h"

//=============================================================================
// 2Dプレイヤークラス
// Author : 唐﨑結斗
// 概要 : 2Dプレイヤー生成を行うクラス
//=============================================================================
class CBullet2D : public CObject2D
{
public:
	//--------------------------------------------------------------------
	// 静的メンバ関数
	//--------------------------------------------------------------------
	// 2Dバレットの生成
	static CBullet2D *Create(const D3DXVECTOR3 &pos, const D3DXVECTOR3 &rot,const float fSpeed);

	//--------------------------------------------------------------------
	// コンストラクタとデストラクタ
	//--------------------------------------------------------------------
	CBullet2D();
	~CBullet2D() override;

	//--------------------------------------------------------------------
	// オーバーライド関数
	//--------------------------------------------------------------------
	HRESULT Init() override;	// 初期化
	void Uninit() override;		// 終了
	void Update() override;		// 更新
	void Draw() override;		// 描画

	//--------------------------------------------------------------------
	// メンバ関数
	//--------------------------------------------------------------------
	D3DXVECTOR3 Move(void);
	void SetSpeed(float fSpeed) { m_fSpeed = fSpeed; }

private:
	//--------------------------------------------------------------------
	// メンバ変数
	//--------------------------------------------------------------------
	D3DXVECTOR3		m_move;					// 移動量
	float			m_fSpeed;				// 移動速度
	int				m_nLife;				// 寿命
};

#endif



