// 概要 : 2Dバレットを生成する
//=============================================================================
CLife * CLife::Create()
{
	// オブジェクトインスタンス
	CLife *pLife = nullptr;

	pLife = new CLife;

	// メモリの確保ができなかった
	assert(pLife != nullptr);

	// 数値の初期化
	pLife->Init();

	// インスタンスを返す
	return pLife;
}

