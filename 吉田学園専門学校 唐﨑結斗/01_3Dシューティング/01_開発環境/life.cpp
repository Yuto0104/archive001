//=============================================================================
// 初期化
// Author : 唐﨑結斗
// 概要 : テクスチャの設定し、メンバ変数の初期値を設定
//=============================================================================
HRESULT CLife::Init()
{// オブジェクト2Dの初期化
	CObject2D::Init();

	// テクスチャの設定
	LoadTex(7);

	// テクスチャ座標の設定
	SetTex(D3DXVECTOR2(0.0f, 0.0f), D3DXVECTOR2(1.0f, 1.0f));

	// サイズの設定
	SetSize(D3DXVECTOR3(50.0f, 50.0f, 0.0f));

	// 状態の設定
	SetLifeState(STATE_NORMAL);

	return S_OK;
}

